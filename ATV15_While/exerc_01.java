package ATV15_While;


public class Exercício_01 {
    public static void main(String[] args) {
        int pecas = 0;
        while (pecas<100) {
            pecas++;
            System.out.println("Peça "+pecas+" montada");
        }
        System.out.println();
        System.out.println("Produção concluída com sucesso.");

    }

}
